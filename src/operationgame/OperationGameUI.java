    private void startBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startBtnActionPerformed
        // TODO add your handling code here:
        
        if(evt.getActionCommand().equals("Start")) {
            generateRandomNums();
            startBtn.setText("Quit");
            startBtn.setBackground(Color.red);
            questionCount = 0;
            score = 0;
            enable();
        } else {
            JOptionPane.showMessageDialog(this, "You have Quit the Game With a Score of " + score +" out of " + GAME_COUNT);
            startBtn.setText("Start");
//            btnAction.setAction(null);
        }
        
        if(evt.getActionCommand().equals("Quit")){
            startBtn.setText("Start");
            startBtn.setBackground(Color.green);
            questionCount = 0;
            score = 0; 
            jlScore.setText("0");
            jlNum1.setText("");
            jlNum2.setText("");
            jlOperation.setText("");
            disable();
        }
        
    }//GEN-LAST:event_startBtnActionPerformed

    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ans = jtfAnswer.getText();
        if(ans.length() == 0) {
            JOptionPane.showMessageDialog(this, "The Answer cannot be empty");
            return;
        }
        
        try  {
            int answer = Integer.parseInt(ans); 
            int realAnswer;
            if(operation == '+') {
                realAnswer = num1 + num2;
            } else if(operation == '-') {
                realAnswer = num1 - num2;
            } else { // operation = '*'
                realAnswer = num1 * num2;
            }
            
            // iF the user entered the correct answer
            if(realAnswer == answer) {
                JOptionPane.showMessageDialog(this, "Great job !!!!! keep up the good work ", "Correct", JOptionPane.INFORMATION_MESSAGE);
                score += 1;
                jtfAnswer.setText(null);
                jlScore.setText(score+"");
            } else {
                JOptionPane.showMessageDialog(this, " Wrong!!!! The Correct answer is " + realAnswer, "WRONG", JOptionPane.ERROR_MESSAGE);
                jtfAnswer.setText(null);
                jtfAnswer.requestFocus();
                // jtfAnswer.selectAll();
                
            }
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "The value entered is not a valid integeer.... Type in numbers not letters");
            return;
        }
        
        questionCount++;
        
        if(questionCount == GAME_COUNT) {
            JOptionPane.showMessageDialog(this, "The Game has ended. You Got a " + score + " out of " +GAME_COUNT );
            int res = JOptionPane.showConfirmDialog(this, "Do you wany to play again?");
            if(res == JOptionPane.YES_OPTION) {
                generateRandomNums();
                startBtn.setText("Quit");
                jlScore.setText("0");
                questionCount = 0;
                score = 0;
                
                 if(res == JOptionPane.NO_OPTION) {
                     System.exit(0);
                 }
                
                
            } else {
                 startBtn.setText("Start");
                 jtfAnswer.setText(null);
                 jlScore.setText("0");
            }
            return;
        }
        
        generateRandomNums();
    }//GEN-LAST:event_jButton2ActionPerformed


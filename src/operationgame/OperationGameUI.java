    private void generateRandomNums() {
        num1 = (int) (Math.random() * 10);
        num2 = (int) (Math.random() * 10);
        if (num1 < num2) {
            int swap = num1;
            num1 = num2;
            num2 = swap;
        }
        int rand = (int) (Math.random() * 3);
        if(rand  == 0)
            operation = '+';
        else if(rand  == 1)
            operation = '-';
        else if(rand  == 2 )
            operation = '*';
        
        
        jlNum1.setText(num1 + "");
        jlNum2.setText(num2 + "");
        jlOperation.setText(operation + "");
    }
    
    
    
